    private void logoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logoutActionPerformed
        Login j = new Login();
        j.setVisible(true);
        this.dispose();

        try {
            String value = Emp.empId;
            String reg = "insert into Audit (emp_id,date,status) values ('" + value + "',now(),'Logged out')";
            pst = conn.prepareStatement(reg);
            pst.execute();
            this.dispose();
        } catch (SQLException e) {

            JOptionPane.showMessageDialog(null, "Mistake : " + e);

        } finally {

            try {
                rs.close();
                pst.close();

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "L:" + e);
            }
        }
    }//GEN-LAST:event_logoutActionPerformed

