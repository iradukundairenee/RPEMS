    public void TrainerInfoDisplay() {
        //txt_emp.setText(currUser.toUpperCase());
        String sqlquery = "SELECT * FROM SYSUSERS WHERE USERNAME=? AND DIVISION=?";
        try {
            pst = conn.prepareStatement(sqlquery);
            pst.setString(1, currUser);
            pst.setString(2, "Trainer");

            rs = pst.executeQuery();
            if (rs.next()) {
                String add1 = rs.getString("username");
                txt_username.setText(add1);

                String add2 = rs.getString("CONTACT");
                txt_tel.setText(add2);

                int add3 = rs.getInt("USERID");
                txt_id.setText(String.valueOf(add3));

                String add4 = rs.getString("division");
                deps.setText(add4);
                String add5 = rs.getString("EMAIL");
                meil.setText(add5);
                String add6 = rs.getString("FIRSTNAME");
                fnm.setText(add6);

                String add7 = rs.getString("LASTNAME");
                lnm.setText(add7);
            }

        } catch (SQLException e) {
        }

    }

