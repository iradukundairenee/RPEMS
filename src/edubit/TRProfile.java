    private void TRCredentials() {
        String currPass = currp.getText();
        String newPass = newp.getText();
        String confirm = confp.getText();
           String currentPassword = getCurrPassword();
        if (currPass.isEmpty() || newPass.isEmpty() || confirm.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Fill all Fields", "BR", 0);
        } else if (!currPass.equals(currentPassword)) {
            JOptionPane.showMessageDialog(null, "Incorrect Current Password", "BR", 0);

        } else if (!newPass.equals(confirm)) {
            JOptionPane.showMessageDialog(null, "New Password Mismatched Confirmation", "BR", 0);
        } else {
            int sure = JOptionPane.showConfirmDialog(null, "Sure To Change Password", "BR", JOptionPane.YES_NO_OPTION);
            if (sure == 0) {
                String query = "SELECT EMAIL FROM SYSUSERS WHERE USERID=?";
                String uptPass = "UPDATE SYSUSERS SET PASSWORD=? WHERE USERID=?";
                int id = Integer.parseInt(txt_id.getText());
                try {
                    pst = conn.prepareStatement(query);
                    pst.setInt(1, id);
                    rs = pst.executeQuery();
                    if (rs.next()) {
                        myEmail = rs.getString("EMAIL");
                    }
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                } finally {
                    try {
                        pst = conn.prepareStatement(uptPass);
                        pst.setString(1, newPass);
                        pst.setInt(2, id);
                        int execute = pst.executeUpdate();
                        if (execute > 0) {
                            JOptionPane.showMessageDialog(null, "Updated", "BR", 1);
                            ClearPassFields();
                        }
                    } catch (HeadlessException | SQLException e) {
                        JOptionPane.showMessageDialog(null,e.getMessage());
                    }
                }
            }

        }

    }

