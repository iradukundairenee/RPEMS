    private String getCurrPassword(){
      
        try {
            int id = Integer.parseInt(txt_id.getText());
            pst = conn.prepareStatement("SELECT PASSWORD FROM SYSUSERS WHERE USERID=?");
            pst.setInt(1,id);
            rs = pst.executeQuery();
            if (rs.next()) {
                myPassword = rs.getString("PASSWORD");
            }
        } catch (NumberFormatException | SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        return myPassword;
    }
