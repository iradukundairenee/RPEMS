    private void TRUpdate() {
        int id = Integer.parseInt(txt_id.getText());
        String division = deps.getText();
        String username = txt_username.getText();
        String firstname = fnm.getText();
        String lastname = lnm.getText();
        String contact = txt_tel.getText();
        String email = meil.getText();

        String sqlquery = "UPDATE SYSUSERS SET USERNAME=?,FIRSTNAME=?,LASTNAME=?,CONTACT=?,EMAIL=? WHERE USERID=?";
        if (username.isEmpty() || firstname.isEmpty() || lastname.isEmpty() || contact.isEmpty() || email.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Empty Field/s", "BR", 0);
        } else {
            try {
                int confirm = JOptionPane.showConfirmDialog(null, "Continue To Update " + username + "'s Profile", "BR Soft", JOptionPane.YES_NO_OPTION);
                if (confirm == 0) {
                    pst = conn.prepareStatement(sqlquery);
                    pst.setString(1, username);
                    pst.setString(2, firstname);
                    pst.setString(3, lastname);
                    pst.setString(4, contact);
                    pst.setString(5, email);
                    pst.setInt(6, id);
                    int exec = pst.executeUpdate();

                    if (exec > 0) {
                        currUser = username;
                        JOptionPane.showMessageDialog(null, "Profile Updated", "BR Soft", JOptionPane.INFORMATION_MESSAGE);
                        TrainerInfoDisplay();
                    }
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }

        }

    }
