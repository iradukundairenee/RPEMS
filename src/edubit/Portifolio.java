    private void Search(String id) {
        try {
            String subsect = (String) tradeCombo.getSelectedItem();
            String clss = (String) levels.getSelectedItem();
            String modn = (String) mod.getSelectedItem();
            String luunm = (String) Units.getSelectedItem();

            String sql = "SELECT REGNO,trainees.FIRST_NAME AS FIRSTNAME,trainees.SURNAME,sectors.SECNAME,trade.TRADENAME,levels.LEVEL,modules.MCODE,modules.ModuleName AS MODULE,learningunits.Unit,learningunits.LU_NAME AS LearningUnit,Qualification,EType,OutOf,obtainedMarks,Percentage,decision,filledDate,sysusers.USERNAME FROM evidence JOIN sectors USING(SECID) JOIN TRADE USING(TRADEID) JOIN LEVELS USING(LEVELID) JOIN modules USING(MCODE) INNER JOIN learningunits ON evidence.LUID=learningunits.LUID INNER JOIN sysusers ON sysusers.USERID=evidence.USERID JOIN trainees USING(REGNO) where TRADENAME=? and LEVEL=? and ModuleName=? and LU_Name=? and REGNO=? LIMIT 50";
            String getSector = "SELECT SECNAME FROM TRADE JOIN SECTORS USING(SECID) WHERE TRADENAME=?";
            pst = conn.prepareStatement(sql);
            pst.setString(1, subsect);
            pst.setString(2, clss);
            pst.setString(3, modn);
            pst.setString(4, luunm);
            pst.setString(5, id);
            rs = pst.executeQuery();

            while (rs.next()) {
                fnm.setText(rs.getString("FIRSTNAME"));
                lnm.setText(rs.getString("SURNAME"));
                sect.setText(rs.getString("SECNAME"));
                trd.setText(rs.getString("TRADENAME"));
                lvl.setText(rs.getString("LEVEL"));
                comp.setText(rs.getString("Qualification"));
                mcode.setText(rs.getString("MCODE"));
                mname.setText(rs.getString("MODULE"));
                String type = rs.getString("Etype");
                String perc = rs.getString("Percentage");
                String dec = rs.getString("decision");
                luName.setText(rs.getString("LearningUnit"));
            }
            try {
                String sql2 = "SELECT trade.TRADENAME,levels.LEVEL,modules.MCODE,modules.ModuleName AS MODULE,learningunits.LU_NAME AS LearningUnit,EType,Percentage,decision FROM evidence JOIN TRADE USING(TRADEID) JOIN LEVELS USING(LEVELID) JOIN modules USING(MCODE) INNER JOIN learningunits ON evidence.LUID=learningunits.LUID INNER JOIN sysusers ON sysusers.USERID=evidence.USERID JOIN trainees USING(REGNO) where TRADENAME=? and LEVEL=? and ModuleName=? and LU_Name=? and REGNO=? and Etype=?";
                String[] Etype = {"Written", "Oral", "Practical", "Performance"};

                for (String type : Etype) {
                    pst = conn.prepareStatement(sql2);
                    pst.setString(1, subsect);
                    pst.setString(2, clss);
                    pst.setString(3, modn);
                    pst.setString(4, luunm);
                    pst.setString(5, id);
                    pst.setString(6, type);
                    rs = pst.executeQuery();
                    switch (type) {
                        case "Written":
                            if (rs.next()) {
                                written.setText(rs.getString("Percentage"));
                                writtenDec.setText(rs.getString("decision"));
                            }
                            break;
                        case "Oral":
                            if (rs.next()) {
                                oral.setText(rs.getString("Percentage"));
                                oralDec.setText(rs.getString("decision"));
                            }
                            break;
                        case "Practical":
                            if (rs.next()) {
                                practical.setText(rs.getString("Percentage"));
                                practicalDec.setText(rs.getString("decision"));
                            }
                            break;
                        case "Performance":
                            if (rs.next()) {
                                performance.setText(rs.getString("Percentage"));
                                performanceDec.setText(rs.getString("decision"));
                            }
                            break;
                        default:
                            break;
                    }

                }
            } catch (SQLException e) {
                System.out.println(e);
                //JOptionPane.showMessageDialog(null, e.getMessage());
            }

        } catch (SQLException e) {
            System.out.print("Caught : " + e.getMessage());
        } finally {

        }
    }

