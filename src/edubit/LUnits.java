    public String GenerateLU() {
        String trad = (String) trd.getSelectedItem();
        String levl = (String) lvl.getSelectedItem();
        String module = (String) mod.getSelectedItem();
        if (trad.equals("Select Trade") || levl.equals("Select Level") || module.equals("Select Module")) {
            learningUnit.setText("");
        } else {
            try {
                String sql = "SELECT COUNT(LUID) AS NUMBERS,TRADE.TRADENAME,levels.LEVEL,modules.ModuleName,Unit,LU_NAME FROM learningunits JOIN trade USING(TRADEID) JOIN levels USING(LEVELID)JOIN modules USING(MCODE) WHERE TRADENAME=? AND LEVEL=? AND ModuleName=? AND Unit!='Summative'";
                pst = conn.prepareStatement(sql);
                pst.setString(1, trad);
                pst.setString(2, levl);
                pst.setString(3, module);
                rs = pst.executeQuery();
                if (rs.next()) {
                    res = (rs.getInt("NUMBERS"));
                }

                int regno = res + 1;
           if(regno>1 & regno<2){
                regno = regno - 1;
           }
                regvalue = "Learning Unit " + String.valueOf(regno);
                learningUnit.setText(regvalue);
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "ErOR" + e);
            }

        }

        return regvalue;
    }

