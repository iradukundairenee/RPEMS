    private void Search(int id) {

        String sql = "SELECT LUID,TRADE.TRADENAME,levels.LEVEL,modules.ModuleName,Unit,LU_NAME FROM learningunits JOIN trade USING(TRADEID) JOIN levels USING(LEVELID)JOIN modules USING(MCODE) WHERE LUID=?";
        try {
            pst = conn.prepareStatement(sql);
            pst.setInt(1, id);
            rs = pst.executeQuery();
            if (rs.next()) {
                int modId = rs.getInt("LUID");
                luId.setText(String.valueOf(modId));

                String trade = rs.getString("TRADENAME");
                trd.removeAllItems();
                trd.addItem(trade);

                String level = rs.getString("LEVEL");
                lvl.removeAllItems();
                lvl.addItem(level);

                String module = rs.getString("ModuleName");
                mod.removeAllItems();
                mod.addItem("Select Module");
                mod.addItem(module);
                mod.setSelectedItem(module);

                String unit = rs.getString("UNIT");
                learningUnit.setText(unit);

                String unitName = rs.getString("LU_NAME");
                txt_unit.setText(unitName);
            }

            //byte[] img = rs.getBytes("Image");
            //imageIcon imageIcon = new ImageIcon(new ImageIcon(img).getImage().getScaledInstance(lbl_img.getWidth(), lbl_img.getHeight(), Image.SCALE_SMOOTH));
            //lbl_img.setIcon(imageIcon);
        } catch (SQLException e) {
            System.out.print("Caught : " + e);
        }
    }

